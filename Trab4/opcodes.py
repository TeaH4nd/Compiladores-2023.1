from enum import Enum

class OpCode(Enum):
    EXIT = 0x00
    NUMBER = 0x01
    ADD = 0x02
    SUB = 0x03
    MUL = 0x04
    DIV = 0x05
    POW = 0x06
    NOT = 0x07
    NEG = 0x08
    EQ = 0x09
    NEQ = 0x0A
    LE = 0x0B
    LEQ = 0x0C
    GE = 0x0D
    GEQ = 0x0E
    LOAD = 0x0F
    STORE = 0x10
    POP = 0x11
    DUP = 0x12
    PRINT = 0x13
    JUMP = 0x14
    JUMP_TRUE = 0x15
    JUMP_FALSE = 0x16
